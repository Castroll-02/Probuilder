    private void btnValidarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnValidarActionPerformed
        if (jTFUsuario.getText().trim().isEmpty()) 
        {
            new frmAlerta("Complete el campo usuario",2).setVisible(true);
        }
        else
        {
            String resultado = sql.obtenerCorreo(jTFUsuario.getText());
            if (!"".equals(resultado)) 
            {
                jTFCorreo.setText(resultado);
                btnEstadoCorreo.setIcon(new javax.swing.ImageIcon(getClass().getResource("/Iconografia/IconCorreoDesbloqueado.png")));
            }
            else
            {
                new frmAlerta("El usuario no tiene correo WTF",3).setVisible(true);
            }
        }
    }//GEN-LAST:event_btnValidarActionPerformed

    private void btnEstadoCorreoActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnEstadoCorreoActionPerformed
        if (!jTFCorreo.getText().trim().isEmpty()) 
        {
            Random r = new Random();
            int codigo = r.nextInt()*8999+1000;
            if (sql.enviarCorreo("<p>Hola solicitaste recuperar tu contrase√±a por medio de correo electronico <br>tu codigo de recuperacion es <b>'"+codigo+"'</b></p>","PROBUILDER COMPANY: Recuperacion de correo electronico", jTFCorreo.getText())) 
            {
                frmValidarCodigo c = new frmValidarCodigo(codigo,jTFUsuario.getText());
                c.setVisible(true);
                this.dispose();
            }
            else
            {
                new frmAlerta("Error al enviar el correo de recuperacion",3).setVisible(true);;
            }
        }
        else
        {
            new frmAlerta("Primero debe verificar su usuario",2).setVisible(true);
        }
    }//GEN-LAST:event_btnEstadoCorreoActionPerformed

